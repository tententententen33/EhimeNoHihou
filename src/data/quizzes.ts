/** 指定ボスのクイズ集を返す（固有問題 + 愛媛全般で約15問に補充。無ければ undefined）。 */
export function getQuiz(bossId: string): QuizQuestion[] | undefined {
  const specific = QUIZZES[bossId];
  if (!specific) return undefined;
  const combined: QuizQuestion[] = [...specific];
  for (const g of GENERAL_EHIME) {
    if (combined.length >= QUIZ_CAP) break;
    if (!combined.some((q) => q.q === g.q)) combined.push(g);
  }
  return combined;
}

/** クイズ集からランダムに 1 問選ぶ。 */
export function pickQuestion(questions: QuizQuestion[], rng: () => number = Math.random): QuizQuestion {
  const i = Math.min(questions.length - 1, Math.floor(rng() * questions.length));
  return questions[i]!;
}
